#include "3-calc.h"
/**
 * op_add - sum of a, b
 * @a: first num
 * @b: second num
 * Return: result of operation
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtract fun a - b
 * @a: first num
 * @b: second num
 * Return: result of operation
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply of a, b
 * @a: first num
 * @b: second num
 * Return: result of operation
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide a / b
 * @a: first num
 * @b: second num
 * Return: result of operation
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - remain of devide a % b
 * @a: first num
 * @b: second num
 * Return: result of operation
*/
int op_mod(int a, int b)
{
	return (a % b);
}
